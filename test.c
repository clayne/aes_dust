/* === utility -------------------------------------------------------*/
static void print_hex(const char *label, const void *buf, size_t len)
{
    const uint8_t *p = (const uint8_t *)buf;
    printf("%s:", label);
    for (size_t i = 0; i < len; ++i) {
        if (i && !(i & 0x0f)) putchar('\n');
        printf(" %02x", p[i]);
    }
    putchar('\n');
}

